/**
 * Uint256 constants
 */

/**
 * Size in bytes (32 bytes for Uint256)
 * @type {32}
 */
export const SIZE = 32;

/**
 * Maximum Uint256 value: 2^256 - 1
 * @type {bigint}
 */
export const MAX = (1n << 256n) - 1n;

/**
 * Minimum Uint256 value: 0
 * @type {bigint}
 */
export const MIN = 0n;

/**
 * Zero value
 * @type {bigint}
 */
export const ZERO = 0n;

/**
 * One value
 * @type {bigint}
 */
export const ONE = 1n;
