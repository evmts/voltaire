import * as Address from "../../../primitives/Address/index.js";

// Example: Derive address from public key
// Uncompressed public key (65 bytes: 0x04 + x + y)
const publicKey = new Uint8Array([
	0x04, 0x50, 0x86, 0x3a, 0xd6, 0x4a, 0x87, 0xae, 0x8a, 0x2f, 0xe8, 0x3c, 0x1a,
	0xf1, 0xa8, 0x40, 0x3c, 0xb5, 0x3f, 0x53, 0xe4, 0x86, 0xd8, 0x51, 0x1d, 0xad,
	0x8a, 0x04, 0x88, 0x7e, 0x5b, 0x23, 0x52, 0x2c, 0xd4, 0x70, 0x24, 0x34, 0x05,
	0x39, 0x7a, 0xb2, 0xbb, 0xc3, 0x68, 0x82, 0xb3, 0x75, 0xd8, 0x44, 0xf8, 0x0c,
	0x32, 0xfa, 0x3d, 0x5f, 0x9d, 0x4f, 0x5e, 0x3a, 0x8a, 0xae, 0x1f, 0x9a, 0x23,
]);

const addr = Address.fromPublicKey(publicKey);
console.log("Address from public key:", addr.toHex());

// From separate x, y coordinates (32 bytes each)
const x =
	0x5086_3ad6_4a87_ae8a_2fe8_3c1a_f1a8_403c_b53f_53e4_86d8_511d_ad8a_0488_7e5b_2352n;
const y =
	0x2cd4_7024_3405_397a_b2bb_c368_82b3_75d8_44f8_0c32_fa3d_5f9d_4f5e_3a8a_ae1f_9a23n;
const addr2 = Address.fromPublicKey(x, y);
console.log("Address from x,y:", addr2.toHex());

// Both methods produce same result
console.log("Addresses match:", addr.equals(addr2));
